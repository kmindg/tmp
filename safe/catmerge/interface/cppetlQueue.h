template <class T> class Queue : public SList <T>
{
public:
    Queue() {};
    Queue(const SListElem <T> &) {};
    ~Queue() {};
    inline void enqueue(SListElem <T> *obj) { this->enqueueTail(obj); return; };
    
};
 
