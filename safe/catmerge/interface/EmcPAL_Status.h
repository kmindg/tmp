/* 
 * the VNX block message catalogs use NT style status codes -
 * whose non-error/error ranges are described below -
 * so we must treat these as non-errors/errors as well in the Linux use case -
 * where we use the CSX status code system
 * so these ranges are (and must be) distinct from the CSX/EmcPAL range used in the Linux use case, so this all works
 *
 * CSX status code system regards
 *    statuses >= CSX_STATUS_NON_SPECIFIC as errors
 *    statuses < CSX_STATUS_NON_SPECIFIC as non-errors
 * CSX status code system starts and ends at these
 *    CSX_STATUS_MARKER_FIRST
 *    CSX_STATUS_MARKER_LAST
 * so make sure that the CSX status code system sits in between these NT-like ranges
 */
#define EMCPAL_MESSAGE_CATALOG_NON_ERROR_STATUS_RANGE_BEGIN ((csx_u32_t)0x01000000)
#define EMCPAL_MESSAGE_CATALOG_NON_ERROR_STATUS_RANGE_END   ((csx_u32_t)0x6FFFFFFF)
#define EMCPAL_MESSAGE_CATALOG_ERROR_STATUS_RANGE_BEGIN     ((csx_u32_t)0x80000000)
#define EMCPAL_MESSAGE_CATALOG_ERROR_STATUS_RANGE_END       ((csx_u32_t)0xFFFFFFFF)
CSX_ASSERT_AT_COMPILE_TIME_GLOBAL_SCOPE(((csx_u32_t)CSX_STATUS_NON_SPECIFIC) > EMCPAL_MESSAGE_CATALOG_NON_ERROR_STATUS_RANGE_END);
CSX_ASSERT_AT_COMPILE_TIME_GLOBAL_SCOPE(((csx_u32_t)CSX_STATUS_NON_SPECIFIC) < EMCPAL_MESSAGE_CATALOG_ERROR_STATUS_RANGE_BEGIN);
CSX_ASSERT_AT_COMPILE_TIME_GLOBAL_SCOPE(((csx_u32_t)CSX_STATUS_MARKER_FIRST) > EMCPAL_MESSAGE_CATALOG_NON_ERROR_STATUS_RANGE_END);
CSX_ASSERT_AT_COMPILE_TIME_GLOBAL_SCOPE(((csx_u32_t)CSX_STATUS_MARKER_LAST) < EMCPAL_MESSAGE_CATALOG_ERROR_STATUS_RANGE_BEGIN);

#define EMCPAL_IS_SUCCESS(_s) CSX_SUCCESS_ALL(_s)
